package pertemuan2;

class PengenalanArray {
    public static void main(String[] args) {
        int[] nilai = new int[5];
        nilai[0] = 10;
        nilai[1] = 5;
        nilai[2] = 3;
        nilai[3] = 4;
        nilai[4] = 1;
        for (int a = 0; a < nilai.length; a++) {
            System.out.println(nilai[a]);
        }
    }
}
